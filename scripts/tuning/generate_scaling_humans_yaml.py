def _group_yaml(solver: str, map_path: str) -> str:
    """Emit a single sweep group for one (solver, map) combination.
    The group's inner sweep varies ``num_humans`` over ``HUMAN_COUNTS``."""
    map_tag = _map_tag(map_path)
    human_counts_inline = "[" + ", ".join(str(n) for n in HUMAN_COUNTS) + "]"
    return (
        f"  # ----- {solver} on {map_tag} -----\n"
        f"  - name: {solver}_{map_tag}\n"
        f"    sweep:\n"
        f"      method:        [ours]\n"
        f"      global_solver: [{solver}]\n"
        f"      map_path:      [{map_path}]\n"
        f"      num_agents:    [{NUM_AGENTS}]\n"
        f"      num_humans:    {human_counts_inline}\n"
        f"      horizon:       [{HORIZON}]\n"
        f"      replan_every:  [{REPLAN_EVERY}]\n"
        f"      fov_radius:    [{PLACEHOLDER_FOV}]\n"
        f"      safety_radius: [{PLACEHOLDER_SAFE}]\n"
    )


def build_yaml() -> str:
    parts = [_header()]
    for solver in SOLVERS:
        for map_path in MAPS:
            parts.append(_group_yaml(solver, map_path))
    return "".join(parts)


# ---------------------------------------------------------------------------
# Verification — re-parse the emitted YAML and compute the cell count
# ---------------------------------------------------------------------------


def verify(yaml_text: str) -> int:
    import yaml
    spec = yaml.safe_load(yaml_text)
    seeds = spec.get("seeds", [])
    total = 0
    for group in spec.get("groups", []):
        sweep = group.get("sweep", {})
        cells = 1
        for axis in ("method", "global_solver", "map_path", "num_agents",
                     "num_humans", "horizon", "replan_every",
                     "fov_radius", "safety_radius"):
            cells *= len(sweep[axis])
        total += cells * len(seeds)
    return total


def main() -> None:
    yaml_text = build_yaml()
    OUT_PATH.parent.mkdir(parents=True, exist_ok=True)
    OUT_PATH.write_text(yaml_text)
    print(f"wrote {OUT_PATH} ({len(yaml_text):,} bytes)")

    expected = (
        len(SOLVERS) * len(MAPS) * len(HUMAN_COUNTS) * len(SEEDS)
    )
    total = verify(yaml_text)
    print(f"groups: {len(SOLVERS) * len(MAPS)}")
    print(f"cell count (re-parsed): {total}")
    print(f"expected:               {expected}")
    if total != expected:
        raise SystemExit(f"FAIL: cell count {total} != {expected}")
    print("OK")


if __name__ == "__main__":
    main()
