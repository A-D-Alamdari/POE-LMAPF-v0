"""DEPRECATED — superseded by the four-sweep split.

  scripts/tuning/generate_scaling_agents_fov3_safe1_yaml.py
  scripts/tuning/generate_scaling_agents_fov4_safe2_yaml.py
  scripts/tuning/generate_scaling_humans_fov3_safe1_yaml.py
  scripts/tuning/generate_scaling_humans_fov4_safe2_yaml.py

See ``scripts/tuning/generate_scaling_agents_yaml.py`` (also deprecated)
for the rationale.  Kept on disk for audit-trail visibility.

----------------------------------------------------------------------

Generate the §5.4 exogenous-density scaling sweep YAML.

Output: ``configs/tuning/scaling_humans.yaml`` — an 840-cell sweep
(6 solvers × 2 maps × 7 num_humans × 10 seeds) sweeping
``num_humans`` at fixed (num_agents, fov, safe, horizon).

Companion to ``generate_scaling_agents_yaml.py``.  The two sweeps
together form the §5.4 controlled-density matrix:

  * scaling_agents:  num_agents varies in {25..250},  num_humans = 100
  * scaling_humans:  num_humans varies in {25..250},  num_agents = 200

Hypothesis under test (§5.4):
    POE-Solver's safety properties (Theorem 1) and throughput
    behavior generalize across Tier-1 MAPF backends and across
    the exogenous-density axis.

Design notes:

* Single method (``ours``); ``global_solver`` axis varies the
  Tier-1 backend.  Tier-2 (buffer-aware controller) held constant.
* ``num_agents`` is fixed at 200 to hold the controlled-agent
  density constant while exogenous density varies.
* ``horizon=40`` and ``replan_every=20`` come from horizon
  tuning (same as scaling_agents).
* ``fov_radius`` and ``safety_radius`` are PLACEHOLDERS until the
  fov/safety sweep analysis completes.
* Group naming: ``<solver>_<map_tag>`` (12 groups; each group's
  inner sweep is the ``num_humans`` axis).

TODO — UPDATE BEFORE LAUNCH
---------------------------

``PLACEHOLDER_FOV`` and ``PLACEHOLDER_SAFE`` must be set from the
fov/safety sweep analysis before launching this sweep.  Workflow
mirrors scaling_agents: pick the (r_fov, r_safe) pair that
maximizes throughput at the §5.4 reference scale subject to
``agent_attr == 0``, edit these two lines, regenerate, launch.

Usage
-----

    python scripts/tuning/generate_scaling_humans_yaml.py

Reproducibility: re-running with identical constants produces a
byte-identical YAML.
"""
